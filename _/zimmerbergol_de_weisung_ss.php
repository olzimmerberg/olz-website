        <td><b>Startprozedere</b></td>
        <td>Der Zugang zum Bereich Massenstart erfolgt über denselben Eingang wie für die nachfolgenden Läufer/innen via Wartezone und Übergaberaum. Jeder Läufer, jede Läuferin löscht und prüft seine, ihre SI-Card frühzeitig beim Eingang zur Wartezone.<br><br>
Die Startläufer/innen stellen sich 5 Min. vor der Startzeit im Bereich Massenstart bereit und treten 2 Min. vor dem Start hinter die Karten. Die Karten dürfen ca. 15 Sek. vor dem Startschuss vom Balken vorsichtig abgelöst, jedoch nicht angehoben oder angeschaut werden.
</td>
    </tr>
    <tr>
        <td><b>Überlauf</b></td>
        <td>Jede Kategorie hat einen Überlauf durchs Festgelände und eine Schlussschlaufe von 3-5 Minuten</td>
    </tr>
    <tr>
        <td><b>Übergabeprozedere</b></td>
        <td>Die ankommenden Läufer/innnen geben nach dem letzten Posten die eigene Laufkarte ab.<br>Bei der Kartenwand lösen sie die Laufkarte für die nachfolgenden Läufer/innen vorsichtig ab (zerreissbar!) und stempeln das Ziel.<br>Danach übergeben sie die Karte den nachfolgenden Läufer/innen, so dass diese die Kartennummer nochmals prüfen können.<br>Nach erfolgter Übergabe gehen sie weiter zum Auslesen der SI-Card.<br><br>
Die Läufer/innen der 2. bis 4. Strecke löschen und prüfen ihre SI-Card selbständig vor dem Betreten der Wartezone.
</td>
    </tr>
